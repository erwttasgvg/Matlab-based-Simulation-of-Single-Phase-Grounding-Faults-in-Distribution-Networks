% =========================================================================
% 算法名称：暂态首半波极性比较法 (Transient Polarity Comparison Method)
% 适用场景：10kV中性点不接地/经消弧线圈接地系统 — 单相接地故障选线
% 适用模型：Distribution_Fault_Model.slx
% 课题方向：配电网单相接地故障选线方法研究
% -------------------------------------------------------------------------
% 算法思路：
%   捕获故障发生瞬间 (暂态过程) 各馈线零序电流的首半波极性。
%   故障馈线的暂态零序电流极性与所有健全馈线相反 (一正多负或一负多正)。
%   暂态分量包含丰富的高频信息，不受消弧线圈补偿的影响。
% =========================================================================

disp('--- 启动暂态极性选线算法 ---');

%% 1. 划定暂态捕获窗口
% 故障发生时刻: t_fault = 0.105s
% 暂态窗口: 故障后 2ms 内 (0.105s ~ 0.107s)
% 选择极短窗口的原因: 仅关注故障发生瞬间的冲击波首半波，避免后续振荡干扰
fault_time  = 0.105;
window_end  = fault_time + 0.002;

%% 2. 调用公共数据提取函数
model_name = 'Distribution_Fault_Model';
disp(['正在运行仿真模型: ', model_name, '.slx...']);
out = sim(model_name);

addpath(fileparts(which(mfilename)));      % 确保公共函数可被找到
[time_axis, i1_data, i2_data, i3_data, i4_data, ~] = extract_simulation_data(out);

%% 3. 提取暂态窗口内各馈线零序电流波形
idx = find(time_axis >= fault_time & time_axis <= window_end);
i1_trans = i1_data(idx);    % 馈线 1 暂态零序电流
i2_trans = i2_data(idx);    % 馈线 2 暂态零序电流
i3_trans = i3_data(idx);    % 馈线 3 暂态零序电流
i4_trans = i4_data(idx);    % 馈线 4 暂态零序电流

%% 4. 特征提取：寻找首半波冲击的极性峰值
% 对每条馈线，在暂态窗口内找到绝对值最大的点，取其带符号的原始值作为极性标记
[~, loc1] = max(abs(i1_trans)); peak1 = i1_trans(loc1);
[~, loc2] = max(abs(i2_trans)); peak2 = i2_trans(loc2);
[~, loc3] = max(abs(i3_trans)); peak3 = i3_trans(loc3);
[~, loc4] = max(abs(i4_trans)); peak4 = i4_trans(loc4);

peaks = [peak1, peak2, peak3, peak4];
fprintf('\n各馈线暂态首半波峰值 (A):\n');
fprintf('馈线 1: %8.4f\n', peaks(1));
fprintf('馈线 2: %8.4f\n', peaks(2));
fprintf('馈线 3: %8.4f\n', peaks(3));
fprintf('馈线 4: %8.4f\n', peaks(4));

%% 5. 噪声过滤：设置最小有效电流门槛
% 幅值低于阈值的信号视为噪声，置零处理，防止噪声影响极性判断
threshold = 0.01;  % 最小有效电流阈值 (A)
valid_peaks = peaks;
valid_peaks(abs(valid_peaks) < threshold) = 0;

%% 6. 极性比较与选线逻辑
% 统计有效峰值中正极性和负极性的数量
pos_count = sum(valid_peaks > 0);   % 正极性馈线数
neg_count = sum(valid_peaks < 0);   % 负极性馈线数

if pos_count == 3 && neg_count == 1
    % 典型模式：3 条正极性 + 1 条负极性 → 负极性的那条为故障线
    [~, faulted_line] = min(valid_peaks);
elseif neg_count == 3 && pos_count == 1
    % 典型模式：3 条负极性 + 1 条正极性 → 正极性的那条为故障线
    [~, faulted_line] = max(valid_peaks);
else
    % 非典型模式：极性特征不明显，退化为暂态幅值比较 (备用判据)
    disp('极性特征不典型，启用暂态幅值备用判据...');
    [~, faulted_line] = max(abs(valid_peaks));
end

%% 7. 输出选线诊断结果
disp('=========================================');
if faulted_line > 0
    fprintf('选线诊断结论：【 馈线 %d 】发生单相接地故障！\n', faulted_line);
else
    disp('未检测到故障或暂态特征微弱无法判定。');
end
disp('=========================================');

%% ========================================================================
%  【原理说明】暂态极性比较法在配电网单相接地故障选线中的应用
%  ========================================================================
%
%  一、基本原理
%  ----------
%  与比幅法和比相法利用"稳态"工频零序电流不同，暂态极性比较法关注的是
%  故障发生瞬间 (0~数毫秒) 产生的暂态冲击电流。
%
%  当单相接地故障发生的瞬间，系统中各馈线的对地分布电容通过故障点产生
%  暂态放电/充电过程。该暂态电流的频率通常为数百 Hz 至数 kHz (远高于
%  工频 50Hz)，具有以下关键特征：
%
%    (1) 故障馈线：暂态零序电流的首半波极性与健全馈线相反。
%        这是因为故障馈线上的电容是通过故障点"放电"，而健全馈线
%        上的电容是通过故障点"充电"，两者电流方向相反。
%
%    (2) 健全馈线：所有健全馈线的暂态零序电流首半波极性相同。
%        它们都在向故障点提供电容充电电流，方向一致。
%
%  因此，如果有 n 条馈线，故障时会出现 "(n-1) 同极性 + 1 反极性" 的模式。
%  那条极性相反的馈线就是故障馈线。
%
%  二、选线判据
%  ----------
%  (1) 在故障后极短的时间窗口 (通常 1~5ms) 内截取各馈线零序电流波形；
%  (2) 提取每条馈线暂态电流中绝对值最大的首半波峰值及其极性 (正/负)；
%  (3) 若 (n-1) 条馈线极性相同、仅 1 条极性相反，则该条为故障馈线；
%  (4) 若极性模式不明显 (如存在噪声或非典型故障)，退化为暂态幅值比较，
%      选择暂态电流幅值最大的馈线作为故障馈线 (备用判据)。
%
%  三、暂态法不受消弧线圈影响的原因
%  ----------
%  消弧线圈 (也称 Petersen 线圈) 的工作原理是在中性点串联电感，提供
%  感性补偿电流来抵消系统的容性零序电流 (稳态工频分量)。
%
%  然而，暂态过程的频率远高于工频 (数百 Hz ~ 数 kHz)。消弧线圈对这些
%  高频暂态分量的阻抗极大 (Z_L = jωL，ω 越大阻抗越大)，几乎无法通过，
%  因此对暂态电流的极性和幅值几乎无影响。
%
%  这使得暂态极性比较法在经消弧线圈接地的系统中依然有效，是其相比稳态
%  方法 (比幅法、比相法) 的核心优势。
%
%  四、优缺点
%  ----------
%  优点：
%    (1) 不受消弧线圈补偿的影响，适用于谐振接地系统；
%    (2) 暂态信号能量集中，信噪比高，选线灵敏度好；
%    (3) 不需要零序电压信息，仅依赖各馈线零序电流即可完成选线；
%    (4) 对高阻接地故障也有一定的识别能力。
%
%  缺点：
%    (1) 对采样率要求较高 (通常 > 10kHz)，需捕捉高频暂态分量；
%    (2) 暂态过程持续时间极短 (数毫秒)，对数据同步和时间窗口定位
%        要求严格，窗口选取不当可能导致误判；
%    (3) 在故障发生角 (故障出现在电压波形的不同相位) 接近过零点时，
%        暂态分量幅值较小，可能影响极性判断的可靠性。
%
%  五、适用范围
%  ----------
%  暂态极性比较法是目前工程中较为先进的选线方法，特别适用于：
%    - 经消弧线圈接地的 10kV/35kV 配电网；
%    - 稳态方法 (比幅法/比相法) 失效的场景；
%    - 具备高采样率数据采集装置的变电站。
%
%  在实际应用中，通常将暂态法与稳态法相结合，构建"多判据融合"的综合
%  选线方案，以提高选线的准确率和可靠性。
%  =========================================================================