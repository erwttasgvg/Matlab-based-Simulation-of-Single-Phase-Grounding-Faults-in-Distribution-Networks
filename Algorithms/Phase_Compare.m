% ==========================================================
% 算法名称：零序电流比相法 (Phase Comparison Method)
% 适用场景：10kV中性点不接地/经消弧线圈接地系统 — 单相接地故障选线
% 适用模型：Distribution_Fault_Model.slx
% 课题方向：配电网单相接地故障选线方法研究
% ----------------------------------------------------------
% 算法思路：
%   利用 FFT 提取各馈线零序电流与母线零序电压的 50Hz 基波相量，
%   计算两者的相位差。故障线路的零序电流方向与健全线路相反，
%   通过识别相位"异号"的线路实现选线。
% ==========================================================

%% 1. 运行 Simulink 仿真模型
disp('正在运行 Simulink 模型，请稍候...');
model_name = 'Distribution_Fault_Model';
addpath(fileparts(which(mfilename)));      % 确保公共函数可被找到
addpath(fullfile(fileparts(which(mfilename)), '..'));  % 将模型所在的上级目录加入搜索路径
out = sim(model_name);

% 定义稳态分析时间窗口 (故障发生于 0.105s)
% 选取 0.15s ~ 0.19s: 故障后约 2 个完整工频周期的稳态段
window_start = 0.150;
window_end   = 0.190;
fs = 20000;   % 采样频率 (Hz)，由仿真步长 5e-05s 决定: 1/5e-05 = 20000
f0 = 50;      % 系统基波频率 (Hz)，中国电网标准频率

%% 2. 调用公共数据提取函数
% 自动兼容 Timeseries / Structure / Array 三种 Simulink 输出格式
[time_axis, i1_data, i2_data, i3_data, i4_data, u0_data] = extract_simulation_data(out);

%% 3. 截取稳态时间窗口内的零序电流与零序电压波形
idx = find(time_axis >= window_start & time_axis <= window_end);

u0_steady = u0_data(idx);   % 母线零序电压 3U0
i1_steady = i1_data(idx);   % 馈线 1 零序电流
i2_steady = i2_data(idx);   % 馈线 2 零序电流
i3_steady = i3_data(idx);   % 馈线 3 零序电流
i4_steady = i4_data(idx);   % 馈线 4 零序电流

%% 4. FFT 频谱分析：提取 50Hz 基波分量的相位角
L = length(u0_steady);       % 截取信号的采样点数

% 对零序电压和各馈线零序电流做快速傅里叶变换 (FFT)
U0_fft = fft(u0_steady);
I1_fft = fft(i1_steady);
I2_fft = fft(i2_steady);
I3_fft = fft(i3_steady);
I4_fft = fft(i4_steady);

% 计算 50Hz 基波在 FFT 结果中对应的数组索引
% 频率分辨率 df = fs / L，50Hz 对应的索引 = f0 / df + 1
target_idx = round(f0 * L / fs) + 1;

% 取出 50Hz 频点的相位角 (弧度 → 角度)
angle_U0 = angle(U0_fft(target_idx)) * 180 / pi;  % 零序电压相位
angle_I1 = angle(I1_fft(target_idx)) * 180 / pi;  % 馈线 1 零序电流相位
angle_I2 = angle(I2_fft(target_idx)) * 180 / pi;  % 馈线 2 零序电流相位
angle_I3 = angle(I3_fft(target_idx)) * 180 / pi;  % 馈线 3 零序电流相位
angle_I4 = angle(I4_fft(target_idx)) * 180 / pi;  % 馈线 4 零序电流相位

%% 5. 计算各馈线零序电流与零序电压的相位差
% Δφ = φ(I0) - φ(U0)，使用 wrapTo180 将结果限制在 [-180°, 180°]
delta_phi_1 = wrapTo180(angle_I1 - angle_U0);
delta_phi_2 = wrapTo180(angle_I2 - angle_U0);
delta_phi_3 = wrapTo180(angle_I3 - angle_U0);
delta_phi_4 = wrapTo180(angle_I4 - angle_U0);

fprintf('\n--- 各馈线零序电流与零序电压的相位差 (50Hz 基波) ---\n');
fprintf('馈线 1: %8.2f°\n', delta_phi_1);
fprintf('馈线 2: %8.2f°\n', delta_phi_2);
fprintf('馈线 3: %8.2f°\n', delta_phi_3);
fprintf('馈线 4: %8.2f°\n', delta_phi_4);

%% 6. 选线逻辑：寻找相位特征与其他线路相反的馈线
phases = [delta_phi_1, delta_phi_2, delta_phi_3, delta_phi_4];

% 选线判据说明:
%   健全线路: I0 超前 U0 约 +90° (容性电流特征)
%   故障线路: I0 滞后 U0 约 -90° (等效为感性，方向相反)
% 因此，相位差最小 (最接近 -90°) 的馈线即为故障线路
[~, fault_line_index] = min(phases);

fprintf('\n>>> 比相法选线结果 <<<\n');
fprintf('判定故障线路为: 馈线 %d\n', fault_line_index);
fprintf('=================================\n');

%% ========================================================================
%  【原理说明】零序电流比相法在配电网单相接地故障选线中的应用
%  ========================================================================
%
%  一、基本原理
%  ----------
%  在中性点不接地系统中，正常运行时三相对地电压对称，零序电压 U0 = 0。
%  发生单相接地故障后，故障相电压降至零，非故障相电压升高至线电压，
%  母线零序电压 3U0 不再为零，其方向由故障相对地电压决定。
%
%  此时各馈线均会流过零序电流 (对地电容电流)：
%    (1) 健全线路的零序电流 = 本线路自身的对地电容电流
%        → 纯容性电流，超前零序电压 U0 约 90°
%        → 即 Δφ ≈ +90°
%
%    (2) 故障线路的零序电流 = 全系统电容电流之和 (方向为流入母线)
%        → 其方向与健全线路相反
%        → 即 Δφ ≈ -90°
%
%  比相法的核心就是：找出零序电流与零序电压相位差为负值 (约 -90°) 的
%  那条馈线，即为故障馈线。
%
%  二、FFT 提取相位的方法
%  ----------
%  本算法使用快速傅里叶变换 (FFT) 提取 50Hz 基波分量的相位角：
%    (1) 对稳态窗口内的信号做 FFT 变换，得到频域表示；
%    (2) 在 FFT 结果中定位 50Hz 对应的索引：k = round(f0 * L / fs) + 1；
%    (3) 用 angle() 函数取出该频点的复数辐角，即为 50Hz 基波的初相角；
%    (4) 将弧度转换为角度 (×180/π)，再计算各馈线 I0 与 U0 的相位差。
%
%  三、选线判据
%  ----------
%  计算 Δφ_k = φ(I0_k) - φ(U0)，对所有馈线 k = 1, 2, ..., n：
%    - 若 Δφ_k ≈ +90°  → 馈线 k 为健全线路 (容性电流，超前 U0)
%    - 若 Δφ_k ≈ -90°  → 馈线 k 为故障线路 (电流方向反转，滞后 U0)
%  选择 Δφ 最小的馈线作为故障线路。
%
%  四、优缺点
%  ----------
%  优点：
%    (1) 利用相位信息选线，不受线路长度和电容大小的影响，
%        灵敏度高于比幅法；
%    (2) 在消弧线圈接地系统中，补偿电流虽改变幅值但不改变相位关系，
%        因此比相法在补偿系统中仍有一定适用性；
%    (3) 理论基础清晰，物理意义明确。
%
%  缺点：
%    (1) 需要同时测量零序电压 U0 和各馈线零序电流 I0，
%        对测量设备要求更高 (需装设零序电压互感器)；
%    (2) FFT 的精度受窗口长度影响，若窗口不是工频周期的整数倍，
%        会产生频谱泄漏，影响相位测量精度；
%    (3) 在高阻接地情况下，零序电压和电流均较小，信噪比低，
%        可能影响选线可靠性。
%
%  五、适用范围
%  ----------
%  适用于中性点不接地及经消弧线圈接地的配电系统。
%  在实际工程中，常与比幅法结合使用，互为验证，提高选线准确率。
%  ========================================================================