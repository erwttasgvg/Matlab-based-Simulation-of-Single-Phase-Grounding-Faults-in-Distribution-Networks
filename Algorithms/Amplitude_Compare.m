% ==========================================================
% 算法名称：零序电流比幅法 (Amplitude Comparison Method)
% 适用场景：10kV中性点不接地/经消弧线圈接地系统 — 单相接地故障选线
% 适用模型：Distribution_Fault_Model.slx
% 课题方向：配电网单相接地故障选线方法研究
% ----------------------------------------------------------
% 算法思路：
%   发生单相接地故障时，故障线路的零序电流等于系统中所有非故障
%   线路对地电容电流之和，因此其幅值远大于任何一条健全线路。
%   本算法通过比较各馈线稳态零序电流的有效值 (RMS) 来识别故障线路。
% ==========================================================

%% 1. 运行 Simulink 仿真模型
disp('正在运行 Simulink 模型，请稍候...');
model_name = 'Distribution_Fault_Model';
addpath(fileparts(which(mfilename)));      % 确保公共函数可被找到
addpath(fullfile(fileparts(which(mfilename)), '..'));  % 将模型所在的上级目录加入搜索路径
out = sim(model_name);

%% 2. 定义稳态分析时间窗口
% 故障发生时刻: t_fault = 0.105s
% 窗口选取: 0.15s ~ 0.19s (故障后约 2 个工频周期的稳态段)
% 选取稳态段的目的: 避开故障瞬间的暂态冲击分量，获得纯净的工频零序电流
window_start = 0.150; 
window_end   = 0.190;   

%% 3. 调用公共数据提取函数
% 自动兼容 Timeseries / Structure / Array 三种 Simulink 输出格式
[time_axis, i1_data, i2_data, i3_data, i4_data, ~] = extract_simulation_data(out);

%% 4. 截取稳态时间窗口内的零序电流波形
idx = find(time_axis >= window_start & time_axis <= window_end);

i1_steady = i1_data(idx);   % 馈线 1 稳态零序电流
i2_steady = i2_data(idx);   % 馈线 2 稳态零序电流
i3_steady = i3_data(idx);   % 馈线 3 稳态零序电流
i4_steady = i4_data(idx);   % 馈线 4 稳态零序电流

%% 5. 特征提取：计算各馈线零序电流有效值 (RMS)
% RMS (均方根值) 是衡量交流信号幅值大小的标准指标
rms1 = rms(i1_steady);
rms2 = rms(i2_steady);
rms3 = rms(i3_steady);
rms4 = rms(i4_steady);

fprintf('--- 稳态零序电流有效值 (RMS) ---\n');
fprintf('馈线 1: %.4f A\n', rms1);
fprintf('馈线 2: %.4f A\n', rms2);
fprintf('馈线 3: %.4f A\n', rms3);
fprintf('馈线 4: %.4f A\n', rms4);

%% 6. 选线判据：寻找零序电流幅值最大的馈线
rms_array = [rms1, rms2, rms3, rms4];
[max_val, fault_line_index] = max(rms_array);

%% 7. 门槛判据：将故障线幅值与健全线平均值对比，防止误判
% 若最大值与均值的比率低于阈值，说明各线路幅值接近，可能无故障
ratio = max_val / mean(rms_array);
fault_threshold = 2.0;  % 比率阈值 (可根据实际仿真情况调整)

fprintf('\n>>> 比幅法选线结果 <<<\n');
if ratio < fault_threshold
    fprintf('系统正常，未检测到明显故障 (比率 = %.2f，阈值 = %.2f)\n', ratio, fault_threshold);
else
    fprintf('判定故障线路为: 馈线 %d (比率 = %.2f)\n', fault_line_index, ratio);
end
fprintf('=================================\n');

%% ========================================================================
%  【原理说明】零序电流比幅法在配电网单相接地故障选线中的应用
%  ========================================================================
%
%  一、基本原理
%  ----------
%  在中性点不接地 (或经消弧线圈接地) 的 10kV 配电网中，每条馈线对地
%  都存在分布电容。正常运行时，三相对地电压对称，三相对地电容电流互差
%  120°，矢量和为零，因此各馈线的零序电流为零。
%
%  当某条馈线 (设为馈线 k) 发生单相接地故障 (例如 A 相接地) 时：
%    - 故障点接地，A 相对地电压降为零，B、C 相对地电压升高至线电压；
%    - 非故障馈线 (馈线 j ≠ k) 的零序电流 = 本线路自身的对地电容电流；
%    - 故障馈线 (馈线 k) 的零序电流 = 所有非故障馈线电容电流之和
%      (方向相反)，即：I_0k = -(I_01 + I_02 + ... + I_0(n-1))。
%
%  因此，故障馈线的零序电流幅值 = 全系统对地电容电流之和 - 自身电容电流，
%  远大于任何一条健全馈线的零序电流。
%
%  二、选线判据
%  ----------
%  比幅法的核心判据非常直观：
%    "零序电流有效值 (RMS) 最大的那条馈线即为故障馈线。"
%
%  本脚本额外引入了 "比率门槛" 判据：
%    ratio = max(I_0_RMS) / mean(I_0_RMS)
%  当 ratio >= 阈值 (默认 2.0) 时，判定故障存在并输出故障馈线编号；
%  当 ratio < 阈值时，认为各馈线零序电流接近，系统无明显故障。
%
%  三、优缺点
%  ----------
%  优点：
%    (1) 原理简单，易于工程实现，计算量小；
%    (2) 对稳态故障具有可靠的选线能力；
%    (3) 不需要安装零序电压互感器 (PT)，仅依赖零序电流即可。
%
%  缺点：
%    (1) 当故障馈线自身的对地电容较大时 (如电缆线路)，故障线与健全线的
%        零序电流幅值差距可能缩小，选线灵敏度降低；
%    (2) 在消弧线圈接地系统中，补偿电流会减小故障线零序电流，可能导致
%        故障线的幅值反而不是最大，造成误选；
%    (3) 对高阻接地故障 (过渡电阻较大) 不够敏感。
%
%  四、适用范围
%  ----------
%  主要适用于中性点不接地系统，馈线数量较多且线路参数差异不大的场景。
%  在消弧线圈接地系统中，需结合其他方法 (如比相法、暂态法) 辅助判断。
%  ========================================================================